// Función helper para formatear MMR
export function formatMMR(mmr: number): string {
    return mmr.toLocaleString('es-ES');
}

// Función helper para obtener win rate (si tienes partidos ganados y totales)
// Nota: Solo tenemos wins, así que esto es una estimación basada en 50-60% WR típico de GC
export function estimateTotalMatches(wins: number): number {
    return Math.round(wins / 0.55); // Asumiendo 55% WR promedio
}

export function calculateWinRate(wins: number, totalMatches: number): number {
    return Math.round((wins / totalMatches) * 100);
}
